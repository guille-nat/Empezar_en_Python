# Variables y constantes en Python

# Constante:
CONSTANTE = 20  # Por convención, las constantes se escriben en mayúsculas

# Variable:
entrada = "Hola"
print(entrada)
entrada = 22
print(entrada)

# Tipos de datos
# Integer
num = 22

# Float
decimal = 1.22

# Boolean
bandera = False

# String
texto = "Franco Colapinto"

# Métodos básicos:
# Contar caracteres en una cadena
apellido = "Fernandez"
print(len(apellido))

# Encontrar el número máximo y mínimo en una lista
numeros = [1, 2, 44, 5, 6, 7, 10]
print(max(numeros))
print(min(numeros))

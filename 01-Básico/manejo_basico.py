
"""Variables, constantes."""
"""Variables: pueden cambiar su conteniido
Constantes: Deberian de tener un contenido fijo"""
# * constante:
x = 20

# * variable:
entrada = "Hola"
print(entrada)
entrada = 22
print(entrada)

"""Tipos de datos."""
"""
Integer: Tipo de dato numérico de caracter ENTERO
Float: Tipo de dato numérico de caracter COMA FLOTANTE
Boolean: Tipo de dato numérico de caracter de dos estados (True, False)
String: Tipo de dato numérico de caracter de CADENA DE TEXTO (conjunto de caracteres)
Char: Tipo de dato numérico de caracter de una sola unidad de texto, este puede ser una letra o un simbolo en forma de string
"""

# * Integer:
num = 22

# * Float:
decimal = 1.22

# * Boolean:
bandera = False

# * String:
texto = "Franco Colapinto"
texto2 = 'Franco Colapinto'

# * Char:
caracter = "A"

"""Metodos propios."""
"""Los metodos propios son aquellas palabras reservadas que cumplen una funcion expecifica o realizan una determinada acción"""

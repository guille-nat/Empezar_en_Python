# Funciones básicas de suma y resta
def suma(num1, num2):
    """Suma dos números y muestra el resultado"""
    print(f'La suma de {num1} y {num2} es {num1 + num2}')


def resta(num1, num2):
    """Resta dos números y muestra el resultado"""
    print(f'La resta de {num1} y {num2} es {num1 - num2}')
